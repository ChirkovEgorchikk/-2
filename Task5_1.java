package Lab2;

public class Task5_1 { // Объявление класса Task5_1, представляющего пистолет
    private int bulletCount; // Приватная переменная для хранения количества патронов

    // Конструктор класса Task5_1, принимающий начальное количество патронов
    // Присваивает переданное количество патронов
    public Task5_1(int initialBullets) {
        this.bulletCount = initialBullets;
    }

    // Конструктор класса Task5_1 без параметров (конструктор по умолчанию)
    // Устанавливает начальное количество патронов по умолчанию равным 5
    public Task5_1() {
        this.bulletCount = 5;
    }

    // Метод для получения текущего количества патронов
    public int getBulletCount() {
        return bulletCount;
    }

    // Метод для имитации выстрела
    public void shoot() {
        if (bulletCount > 0) { // Проверяет, есть ли патроны
            System.out.println("Бах!"); // Выводит звук выстрела
            bulletCount--; // Уменьшает количество патронов на 1
        } else {
            System.out.println("Клац!"); // Выводит звук холостого выстрела (нет патронов)
        }
    }

    // Метод для проверки, заряжен ли пистолет
    public boolean isLoaded() {
        return bulletCount > 0;
    }

    // Переопределение метода toString() для вывода информации о пистолете
    @Override
    public String toString() {
        // Возвращает строковое описание пистолета, включая количество патронов и его состояние (заряжен/разряжен)
        return "Пистолет {" +
                "патронов=" + bulletCount +
                ", состояние=" + (isLoaded() ? "заряжен" : "разряжен") +
                '}';
    }
}